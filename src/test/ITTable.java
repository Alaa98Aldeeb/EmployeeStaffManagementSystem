            Connection connection = DriverManager.getConnection(urlDb, userNameDb, passDb);
            Statement statement = connection.createStatement();
            String Query = "insert into IT_Employee_Table (ID,FirstName,LastName,Gender,DateOfBirth,Email,PhoneNumber,Address,HireDate,Salary, Department) Values ('"
                    + Integer.parseInt(idTxt.getText()) + "' , '" + firstNameTxt.getText() + "' , '" + lastNameTxt.getText() + "' , '" + genderGroup.getSelection().getActionCommand() + "' , '" + birthDateFormat
                    + "' , '" + emailTxt.getText() + "' , '" + Integer.parseInt(phoneTxt.getText()) + "' , '" + addressTxt.getText() + "' , '" + hireDateFormat + "' , '" + Float.parseFloat(salTxt.getText()) + "' , '" + "HR')";
