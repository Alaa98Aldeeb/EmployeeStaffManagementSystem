    private void serchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serchBtnActionPerformed
        DefaultTableModel defaultTableModel = (DefaultTableModel) ITTable.getModel();
        defaultTableModel.setRowCount(0);
        try {
            String urlDb = "jdbc:oracle:thin:@localhost:1521:ORCL2";
            String userNameDb = "sys as sysdba";
            String passDb = "1234";

            Connection connection = DriverManager.getConnection(urlDb, userNameDb, passDb);
            Statement statement = connection.createStatement();
            String Query = "select * from IT_Employee_Table where ID = '" + Integer.parseInt(idTxt.getText()) + "'";
            ResultSet rs = statement.executeQuery(Query);
            
            while (rs.next()) {
                String id = String.valueOf(rs.getInt("ID"));
                String fn = String.valueOf(rs.getString("FirstName"));
                String ln = String.valueOf(rs.getString("LastName"));
                String genderType = String.valueOf(rs.getString("Gender"));
                String birthDate = String.valueOf(rs.getString("DateOfBirth"));
                String email = String.valueOf(rs.getString("Email"));
                String phone = String.valueOf(rs.getInt("PhoneNumber"));
                String address = String.valueOf(rs.getString("Address"));
                String hireDate = String.valueOf(rs.getString("HireDate"));
                String sal = String.valueOf(rs.getInt("Salary"));

                String tableData[] = {id, fn, ln, genderType, birthDate, email, phone, address, hireDate, sal};
                defaultTableModel = (DefaultTableModel) ITTable.getModel();
                defaultTableModel.addRow(tableData);
                
                return;
            }

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Employee with entered data not exsit");
                refreshTable();
            }
            

            idTxt.setText("");
            firstNameTxt.setText("");
            lastNameTxt.setText("");
            femaleRbtn.setSelected(false);
            maleRbtn.setSelected(false);
            emailTxt.setText("");
            phoneTxt.setText("");
            addressTxt.setText("");
            salTxt.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_serchBtnActionPerformed

