    private void workersTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_workersTableMouseClicked
       DefaultTableModel defaultTableModel = (DefaultTableModel) workersTable.getModel();
        int rowIndex = workersTable.getSelectedRow();

        idTxt.setText(defaultTableModel.getValueAt(rowIndex, 0).toString());
        firstNameTxt.setText(defaultTableModel.getValueAt(rowIndex, 1).toString());
        lastNameTxt.setText(defaultTableModel.getValueAt(rowIndex, 2).toString());
        if (defaultTableModel.getValueAt(rowIndex, 3).toString().equals("Female")) {
            femaleRbtn.setSelected(true);
        } else {
            maleRbtn.setSelected(true);
        }
        emailTxt.setText(defaultTableModel.getValueAt(rowIndex, 5).toString());
        phoneTxt.setText(defaultTableModel.getValueAt(rowIndex, 6).toString());
        addressTxt.setText(defaultTableModel.getValueAt(rowIndex, 7).toString());
        salTxt.setText(defaultTableModel.getValueAt(rowIndex, 9).toString());

        SimpleDateFormat parser = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat formatter = new SimpleDateFormat("MMM DD, YY");
        Date birthDate, hireDate;
        try {
            birthDate = parser.parse(defaultTableModel.getValueAt(rowIndex, 4).toString());
            hireDate = parser.parse(defaultTableModel.getValueAt(rowIndex, 8).toString());

            birthDareCal.setDate(birthDate);
            hireDateCal.setDate(hireDate);
        } catch (ParseException ex) {
            Logger.getLogger(HrTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_workersTableMouseClicked

